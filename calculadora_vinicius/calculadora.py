import re


def Soma(n1, n2):
  resultado = n1 + n2
  return resultado

def Subtracao(n1, n2):
  resultado = n1 - n2
  return resultado

def Divisao(n1, n2):
  resultado = n1 / n2
  return resultado

def Multiplicacao(n1, n2):
  resultado = n1 * n2
  return resultado

def Resto(n1, n2):
  resultado = n1 % n2
  return resultado